package leetcode

func inorderTraversalOld(root *TreeNode) []int {
	ans := make([]int,0)

	var preOrder func(node *TreeNode)
	preOrder = func(root *TreeNode) {
		if root == nil {
			return
		}
		preOrder(root.Left)
		ans = append(ans, root.Val)
		preOrder(root.Right)

	}

	preOrder(root)
	return ans
}

