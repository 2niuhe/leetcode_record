package leetcode

func preorderTraversalOld(root *TreeNode) []int {
	ans := make([]int,0)

	var preOrder func(node *TreeNode)
	preOrder = func(root *TreeNode) {
		if root == nil {
			return
		}
		ans = append(ans, root.Val)
		preOrder(root.Left)
		preOrder(root.Right)
	}

	preOrder(root)
	return ans
}

