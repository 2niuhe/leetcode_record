func containsDuplicate(nums []int) bool {
	hashRecord := make(map[int]int,0)

	for _,num := range nums {
		_,ok := hashRecord[num]
		if ok {
			return true
		} else {
			hashRecord[num] = 0
		}
	}
	return false
}
